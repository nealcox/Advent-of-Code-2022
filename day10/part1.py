import sys


def main():
    filename = "input.txt"
    if len(sys.argv) > 1:
        filename = sys.argv[1]
    with open(filename) as f:
        input_text = f.read().strip()
    print(f"Answer: {calculate(input_text)}")


def calculate(input_text):

    cycles = 0
    X = 1
    hist = {0: X}
    for line in input_text.splitlines():
        inst = line.split()
        if inst[0] == "noop":
            cycles += 1
        elif inst[0] == "addx":
            X += int(inst[1])
            cycles += 2
        else:
            raise ValueError(inst)
        hist[cycles] = X

    answer = 0
    for i in (20, 60, 100, 140, 180, 220):
        if i - 1 in hist.keys():
            answer += hist[i - 1] * i
        else:
            answer += hist[i - 2] * i
    return answer


